
use ch11_tests::reverse_string;


#[test]
fn int_reverse_string_int() {
    assert_eq!(reverse_string("hello"), "olleh");
    assert_eq!(reverse_string("Rust"), "tsuR");
    assert_eq!(reverse_string(""), "");
}



#[test]
fn int_reverse_string_int2() {
    assert_eq!(reverse_string("hello"), "olleh");
    assert_eq!(reverse_string("Rust"), "tsuR");
    assert_eq!(reverse_string(""), "");
}